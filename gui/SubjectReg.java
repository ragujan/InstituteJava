    public void tableListernRag() {
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int row = jTable1.getSelectedRow();
                if (row != -1) {

                    String sid = jTable1.getValueAt(row, 0).toString();
                    String sname = jTable1.getValueAt(row, 1).toString();
                    String sdesc = jTable1.getValueAt(row, 2).toString();
                    String sprice = jTable1.getValueAt(row, 3).toString();
                    subId = sid;
                    if(isTeacherInvoled){
                        tr.jComboBox3.setSelectedItem(sname);
                        sr.dispose();
                    }
                    jTextField1.setText(sname);
                    jTextField2.setText(sdesc);
                    jTextField3.setText(sprice);
                    jButton1.setEnabled(false);
                    jButton2.setEnabled(true);
                    isUpdateDesc = true;
                }
            }

        });

    }

