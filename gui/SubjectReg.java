    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JTextField[] jc = {jTextField1, jTextField2, jTextField3};
        boolean isEmptyDetected = false;
        //validations are already done so you just have to check wheather the fields are empty or not
        for (int i = 0; i < jc.length; i++) {

            JTextField jt = jc[i];
            if (jt.getText().isEmpty()) {
                isEmptyDetected = true;
                JOP.setJOPMessage(this, "Empty Input Field", "Empty Values", 1);
                break;
            }

        }

        if (!isEmptyDetected) {
            boolean isSameSubject = false;
            try {
                ResultSet rs = MySql.sq("SELECT * FROM `subject` WHERE `subject`.`subject_name`='" + jTextField1.getText() + "'");
                if (rs.next()) {
                    isSameSubject = true;
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(SubjectReg.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(SubjectReg.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (!isSameSubject) {
                ArrayList<String> ar = new ArrayList<String>();
                ar.add(jc[1].getText());
                ar.add(jc[0].getText());
                ar.add(jc[2].getText());
                InsertTable it = new InsertTable("subject", ar);
                loadTable();
            } else {
                JOP.setJOPMessage(this, "This subject name is already exists", "Duplicate Subject Name", 1);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

